import math

x = 2
y = 10

print("{} + {} = {}".format(x, y, x + y))
print("{} - {} = {}".format(x, y, x - y))
print("{} * {} = {}".format(x, y, x * y))
print("{} / {} = {}".format(x, y, x / y))
print("{} ** {} = {}".format(x, y, x ** y))
print("log({}) = {}".format(x, math.log(x, 10)))
print("log({}) = {}".format(y, math.log(y, 10)))
